    private void spinnerNumStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_spinnerNumStateChanged
        // TODO add your handling code here:
        lblNum.setText(spinnerNum.getValue().toString());
        long n1, f, cont1;
        Integer cont2 = 0;
        String fatorial = "";
        n1 = Long.parseLong(spinnerNum.getValue().toString());
        f = 1;
        cont1 = 1;
        while (cont1<n1) {
            cont1 ++;
            f *= cont1;
        }
        lblResultado.setText(Long.toString(f));
        while (cont2 != n1) {
            cont2 ++;
            fatorial += cont2.toString();
            if (cont2 >= 1 && cont2 != n1) {
                fatorial += " x ";
            } else {
                fatorial += " = " + f;
            }
        lblFatorial.setText(fatorial);
        }
    }//GEN-LAST:event_spinnerNumStateChanged

