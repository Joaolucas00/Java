    private void btnVerificaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificaActionPerformed
        // TODO add your handling code here:
        int a, b, c;
        a = sliderA.getValue();
        b = sliderB.getValue();
        c = sliderC.getValue();
        if (a<b+c && b<a+c && c<b+a) {
            lblStatus.setText("Forma um Tri√¢ngulo");
            if (a == b && b == c) {
                lblTipo.setText("Equil√°treo");
            } else if (a == b && b != c || c == b && b != a) {
                lblTipo.setText("Is√≥sceles");
            } else {
                lblTipo.setText("Escaleno");
            }
        } else {
            lblStatus.setText("N√£o Forma um Tri√¢ngulo");
            lblTipo.setText("----");
        }
        panelResultado.setVisible(true);
    }//GEN-LAST:event_btnVerificaActionPerformed

